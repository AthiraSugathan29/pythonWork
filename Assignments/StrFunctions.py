# String Functions

# 1) Capitalize
print("1) Capitalize")
str1 = "welcome to python"
print(str1.capitalize())

# 2) Upper Case
print("2) Upper Case")
print(str1.upper())

# 3) Lower Case
print("3) Lower Case")
str2 = "PYTHON"
print(str2.lower())

# 4) Swap Case
print("4) Swap Case")
str3 = "HelLO wOrLD"
print(str3.swapcase())

# 5) Title Case
print("5) Title Case")
str4 = "welcome to python"
print(str4.title())

# 6) Count
print("6) Count")
print(str4.count("t"))
print(str4.count("t",0,9))

# 7) islower
print("7) islower")
str5 = "python"
print(str5.islower())

# 8) isupper
print("8) isupper")
str6 = "PYTHON"
print(str6.isupper())

# 9) isalpha
print("9) isalpha")
str7 = "Python7"
print(str6.isalpha())
print(str7.isalpha())

# 10) isalnum
print("10) isalnum")
print(str6.isalnum())
print(str7.isalnum())

# 11) find
print("11) find")
fruit = "Banana"
print(fruit.find("na"))
print(fruit.find("z"))

# 12) replace
print("12) replace")
fruit = "apple is red"
print(fruit)
print(fruit.replace("e","X"))
print(fruit.replace("e","X",1))




















