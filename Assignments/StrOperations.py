# To Perform all string operations

#Indexing
str1 = "Hello World"
print("Indexing")
print(str1[0])
print(str1[8])
print("Reverse Indexing")
print(str1[-1])
print(str1[-5])

#Slicing
str2 = "Test String"
print("Positive Slicing")
print(str2[1:])
print(str2[0:3])
print(str2[1:8])

print("Negative Slicing")
print(str2[:-2])
print(str2[:-4])

print("Reverse Slicing")
print(str2[::-1])
print(str2[::-2])
print(str2[::-3])