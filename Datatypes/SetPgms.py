# Set datatype
set1 = set() # empty set declaration
set1 = {1,2.3,5,6}
print(set1)

set_val = {2,3.4,5,(1,3,8)} # set with multiple datatype & set doesnot allow list datatype as value
print(set_val)

set2 = set([9,1,8]) # Set doesnot allow list datatype as value & we can use set() to declare lists as value in a set.
print(set2)

