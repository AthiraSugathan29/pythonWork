#Membership operators
# in,not in

a = ["apple","mango"]

print("apple" in a) #Returns True, "apple" present in a
print("grapes" in a) #Returns False, "grapes" not present in a

print("apple" not in a) #Returns False, "apple" present in a
print("grapes" not in a) #Returns True, "grapes" not present in a